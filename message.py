
def messagem_erro(mensagem):
    mensagem_atual = f"""############## ERROR ############### \
    \n{mensagem}\n############ FIM ERROR #############"""
    return mensagem_atual


def messagem_sucesso(mensagem):
    mensagem_atual = f"""############## SUCESSO ############### \
    \n{mensagem}\n############ FIM SUCESSO #############"""
    return mensagem_atual
